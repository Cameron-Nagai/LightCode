package com.symmetrylabs.layouts.tree.config;

public class TwigConfig {
    float x;
    float y;
    float z;
    float theta;
    float tilt;
    int wiringIndex;

    public TwigConfig(float x, float y, float z, float theta, float tilt, int wiringIndex) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.theta = theta;
        this.tilt = tilt;
        this.wiringIndex;
    }
}