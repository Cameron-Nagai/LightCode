package com.symmetrylabs.shows.vines;

public class LeafConfig {
	float xPos, yPos, zPos, xRot, yRot, zRot;

	public LeafConfig(float xPos, float yPos, float zPos, float xRot, float yRot, float zRot) {
		this.xPos = xPos;
		this.yPos = yPos;
		this.zPos = zPos;
		this.xRot = xRot;
		this.yRot = yRot;
		this.zRot = zRot;
	}
}